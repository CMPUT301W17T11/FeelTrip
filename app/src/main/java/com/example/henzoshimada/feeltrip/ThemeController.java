public class ThemeController {
    private int themeSelection;

    /**
     * Instantiates a new Theme controller.
     */
    public ThemeController() {
    }

    /**
     * Gets theme selection.
     *
     * @return the theme selection
     */
    public int getThemeSelection() {
        return themeSelection;
    }

    /**
     * Sets theme selection.
     *
     * @param themeSelection the theme selection
     */
    public void setThemeSelection(int themeSelection) {
        this.themeSelection = themeSelection;
    }
}
